import pandas as pd

# 배점 기준
data1 = {
    '센터코드': ['LA', 'LA', 'LA', 'KS', 'KS', 'KS', 'OTC', 'OTC', 'OTC', 'LA', 'LA', 'LA', 'KS', 'KS', 'KS', 'OTC', 'OTC',
             'OTC'],
    '항목': ['S01', 'S01', 'S01', 'S01', 'S01', 'S01', 'S01', 'S01', 'S01', 'Q01', 'Q01', 'Q01', 'Q01', 'Q01', 'Q01',
           'Q01', 'Q01', 'Q01'],
    '평가': ['상', '중', '하', '상', '중', '하', '상', '중', '하', '상', '중', '하', '상', '중', '하', '상', '중', '하'],
    '점수': ['10', '9', '8', '7', '6', '5', '4', '3', '2', '100', '90', '80', '70', '60', '50', '40', '30', '20']
}

df1 = pd.DataFrame(data1)

# 결과 테이블
data2 = {
    '센터코드': ['LA', 'KS', 'KS', 'OTC', 'OTC', 'OTC', 'LA', 'KS', 'KS', 'OTC', 'OTC', 'OTC'],
    '항목': ['S01', 'S01', 'S01', 'S01', 'S01', 'S01', 'Q01', 'Q01', 'Q01', 'Q01', 'Q01', 'Q01'],
    '이름': ['AA', 'BB', 'CC', 'DD', 'EE', 'FF', 'AA', 'BB', 'CC', 'DD', 'EE', 'FF'],
    '평가': ['상', '중', '하', '상', '중', '하', '상', '중', '하', '상', '중', '하']
}

df2 = pd.DataFrame(data2)

# 각 항목에 대한 평가에 따른 점수를 배정
score_mapping = df1.set_index(['센터코드', '항목', '평가'])['점수'].to_dict()

# 결과 테이블에 새로운 컬럼으로 추가
df2['점수'] = df2.apply(lambda row: score_mapping.get((row['센터코드'], row['항목'], row['평가']), None), axis=1)

# 최종 결과 출력
print(df2)
